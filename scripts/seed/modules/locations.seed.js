class LocationsSeed {
  constructor() {
    this.logger = new SeedLogger('Locations');
    this.mongoose = mongoose;
  }

  async execute() {
    try {
      this.logger.info('📍 Seeding Locations — 36 states/UTs, 700+ cities, 6000+ pincodes');
      const conn = this.mongoose.connection;
      if (!conn || !conn.collection) throw new Error('MongoDB connection not available');

      await conn.collection('adminstates').deleteMany({});
      await conn.collection('admincities').deleteMany({});
      await conn.collection('adminzipcodes').deleteMany({});

      const indiaDoc = await conn.collection('admincountries').findOne({ code: 'IN' });
      if (!indiaDoc) throw new Error('India country record not found — run countries seed first');
      const indiaId = indiaDoc._id;

      // ── Insert States ────────────────────────────────────────────────────────
      const stateDocs = STATES.map(s => ({
        _id: new mongoose.Types.ObjectId(),
        name: s.name,
        countryId: indiaId,
        gstCode: s.gstCode,
        stateCode: s.code,
        region: s.region,
        tier: s.tier,
        active: true,
        createdAt: new Date(),
        updatedAt: new Date(),
      }));
      await conn.collection('adminstates').insertMany(stateDocs);
      this.logger.recordBatch(stateDocs.length);

      const stateIdMap = {};
      stateDocs.forEach(s => { stateIdMap[s.stateCode] = s._id; });

      // ── Insert Cities ────────────────────────────────────────────────────────
      const cityDocs = [];
      for (const stateDoc of stateDocs) {
        const cities = CITIES_BY_STATE[stateDoc.stateCode];
        if (!cities) continue;
        for (const c of cities) {
          cityDocs.push({
            _id: new mongoose.Types.ObjectId(),
            name: c.name,
            stateId: stateDoc._id,
            stateCode: stateDoc.stateCode,
            countryId: indiaId,
            latitude: c.lat,
            longitude: c.lng,
            tier: c.tier,
            isMajor: c.isMajor,
            active: true,
            createdAt: new Date(),
            updatedAt: new Date(),
          });
        }
      }
      for (let i = 0; i < cityDocs.length; i += 200) {
        await conn.collection('admincities').insertMany(cityDocs.slice(i, i + 200));
      }
      this.logger.recordBatch(cityDocs.length);

      // ── Insert ZipCodes (per-city, proper relationship) ──────────────────────
      const zipDocs = [];
      let areaIdx = 0;

      for (const cityDoc of cityDocs) {
        const key = `${cityDoc.stateCode}:${cityDoc.name}`;
        const ranges = PINCODES_BY_CITY[key];
        if (!ranges) continue;

        const isTier1 = cityDoc.tier === 1;
        const isTier2 = cityDoc.tier === 2;

        for (const [start, end] of ranges) {
          for (let pin = start; pin <= end; pin++) {
            const jitter = () => (Math.random() - 0.5) * 0.08;
            zipDocs.push({
              _id: new mongoose.Types.ObjectId(),
              zipCode: String(pin),
              areaName: AREA_NAMES[areaIdx % AREA_NAMES.length],
              countryId: indiaId,
              stateId: cityDoc.stateId,
              cityId: cityDoc._id,
              latitude: cityDoc.latitude + jitter(),
              longitude: cityDoc.longitude + jitter(),
              serviceable: true,
              codAvailable: isTier1 || isTier2 || Math.random() > 0.2,
              expressDelivery: isTier1,
              deliveryCharge: isTier1 ? 0 : isTier2 ? 40 : 80,
              minOrderAmount: isTier1 ? 0 : isTier2 ? 199 : 499,
              estimatedDeliveryDays: isTier1 ? 1 : isTier2 ? 3 : 5,
              active: true,
              createdAt: new Date(),
              updatedAt: new Date(),
            });
            areaIdx++;
          }
        }
      }

      for (let i = 0; i < zipDocs.length; i += 500) {
        await conn.collection('adminzipcodes').insertMany(zipDocs.slice(i, i + 500));
      }
      this.logger.recordBatch(zipDocs.length);

      this.logger.printStats();
      return {
        created: stateDocs.length + cityDocs.length + zipDocs.length,
        states: stateDocs.length,
        cities: cityDocs.length,
        pincodes: zipDocs.length,
      };
    } catch (error) {
      this.logger.error('Locations seeding failed', error);
      throw error;
    }
  }
}

module.exports = LocationsSeed;
